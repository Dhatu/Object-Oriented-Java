package SumofNumber;

public class SumofNumber {
	 public static int getSum(int sum,int input) {
		// loop through the digits from 1 to said input
			for ( int number = 1; number <= input; number++){
				sum = sum + number;
				// recognize the previous of integer to next integer until it equals input
			}
			return sum;//return statement for get method 
	 }
}
